        `演员编号` = ( SELECT `演员编号` FROM `电影信息表`, `参演表` WHERE `电影信息表`.`电影编号` = `参演表`.`电影编号` AND `是否为主演` = 1 AND `电影信息表`.`电影编号` = ID );
ELSE SELECT
         '没有该电影';

END IF;

END
;;

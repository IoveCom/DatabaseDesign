BEGIN
DECLARE
ID VARCHAR ( 10 );
	
	SET ID = ( SELECT `电影编号` FROM `电影信息表` WHERE `电影名` = `查询电影` );
	IF
( ID IS NOT NULL ) THEN
SELECT
    `演员姓名`
